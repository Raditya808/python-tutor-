# tipe data adalah tipe data yang digunakan untuk menyimpan nilai dalam variabel

# macam macam tipe data

# tipe data angka satuan (integer)

# ini adalah contoh tipe data integer
# integer adalah tipe data yang digunakan untuk menyimpan bilangan bulat
data_integer = 1
print("data: ", data_integer)
print("- bertipe : ", type(data_integer))

print("==========================")

# ini adalah contoh tipe data float / berkoma
# tipe data angka dengan koma (float)
data_float = 1.5
print("data: ", data_float)
print("- bertipe : ", type(data_float))

print("==========================")

# tipe data: kumpulan karakter (string)
data_string = "1.5"
print("data: ", data_string)
print("- bertipe : ", type(data_string))
print("==========================")

# tipe data biner true/false (boolean)
data_bool = False
print("data: ", data_bool)
print("- bertipe : ", type(data_bool))
print("==========================")

# tipe data khusus

# bilangan kompleks (complex)
data_bool_complex = complex(5, 6)
print("data: ", data_bool_complex)
print("- bertipe : ", type(data_bool_complex))
print("===========================")

